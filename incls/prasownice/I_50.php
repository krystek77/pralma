        <h2>Praswonice walcowe, susząco-prasujące</h2>
        <h3>podgrzew elektryczny, gazowy, parowy</h3>

    <p class="introduction">
        Nowa generacja prasownic walcowych, serii I50 jest odpowiedzią na rosnące wymagania
        pralni przemysłowych. Dzięki przedniemu wywiezieniu prania, prasownice mogą być
        obsługiwane przez 1 osobę. Serwisowanie prasownic nie jest wymagane. Prostota obsługi, niezawodność,
        wydajność oraz wysoka jakość prasowania - to cechy, które bez wątpienia charakteryzują serię I50.
        Elementy grzewcze umieszczono wokół wałka co zapewnia równomierną temperaturę na całej powierzchni a
        w połączeniu z 3 czujnikami temperatury, zapewnia równomierne prasowanie prania. Kąt opasania to ponad 300
        <sup>0</sup>, dzięki czemu prasuje się szybko. Dodatkowo pasy prasująca wykonane są w standardzie z
        trwałego <b>Nomex'u</b> o długiej żywotności i dużej wytrzymałości na wysoką temperaturę. Cienki, ciężki walec
        zapewnia szybką reakcję na zmianę temperatury i doskonałe rezultaty prasowania.
    </p>

